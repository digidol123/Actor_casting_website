<h2>About us</h2>
<p>Casting Aber, is a website that helps the student filmmakers from the Department of Theatre, Film & Television Studies (TFTS) at Aberystwyth University to look for suitable actors to take part in their films. </p>

<p>
The Department of Theatre, Film and Television Studies(TFTS), part of the Institute of Arts and Humanities, is uniquely located to provide high-quality learning, teaching and research opportunities for students drawn from the populations of Wales, the United Kingdom, Europe and beyond. As one of the largest and most significant departments of its kind, its historic mission is to address the diverse needs and aspirations of its stakeholders and constituents. The Department of Theatre, Film and Television Studies plays a key role in developing talented individuals who go on to excel within the Creative Industries.
</p>

